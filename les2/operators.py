a = 5
b = 3
print("a= ", a)
print("b= ", b)
print("a > b ", a > b)
print("a == b ", a == b )
print("a % b = ", a % b)
x = 0
y = 1
print("x = ", x, "; y = ", y)
print("x | y - битовая операция ИЛИ, объединение множеств ")
print(x | y)
print("x ^ y - битовая операция «исключающее ИЛИ» ")
print(x ^ y)
print("x & y - битовая операция И ")
print(x & y)
print("x << y, x >> y - сдвиг значения x влево или вправо на y битов ")
print("x << y = ", x << y, "x >> y = ", x >> y)

t = (3, 2, 1)
print("b in t = ",t," = ", b in t)
print("~x - битовая операция НЕ (инверсия) = ", ~x)
print("~y - битовая операция НЕ (инверсия) = ", ~y)
